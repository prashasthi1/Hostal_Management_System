    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try 
        {
            // TODO add your handling code here:
            JasperDesign jdesign=JRXmlLoader.load("D:\\NIBM DSE\\2nd sem 2nd stage\\netbeans practical\\Hostel Management system\\src\\fees.jrxml");
            String query="select *from fees";
            
            JRDesignQuery updateQuery=new JRDesignQuery();
            updateQuery.setText(query);
            
            jdesign.setQuery(updateQuery);
            
            JasperReport jreport=JasperCompileManager.compileReport(jdesign);
            JasperPrint jprint=JasperFillManager.fillReport(jreport, null,ConnectionProvider.getCon());
            
            JasperViewer.viewReport(jprint);
        } 
        catch (JRException ex) 
        {
            Logger.getLogger(StudentFees.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

