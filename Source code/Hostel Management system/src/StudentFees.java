    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String IDNo=jTextField1.getText();
        //String mobileNo=jTextField2.getText();
        //String name=jTextField3.getText();
        SimpleDateFormat dFormat=new SimpleDateFormat("MM-YYYY");
        Date date=new Date();
        String month=dFormat.format(date);
        try
        {
          Connection con=ConnectionProvider.getCon();
          Statement st=con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_READ_ONLY);
          ResultSet rs=st.executeQuery("select *from student where IDNo='"+IDNo+"' and status='living'");  
          if(rs.first())
          {
            jTextField1.setEditable(false);
            jTextField2.setText(rs.getString(2));
            jTextField3.setText(rs.getString(3));
            jTextField4.setText(rs.getString(5));
            jTextField5.setText(rs.getString(10));
            jTextField6.setText(month);
            jTextField7.setText("10000");
          }
          else
          {
            JOptionPane.showMessageDialog(null,"Student does not Exist");
            clear();
          }
          tableDetails();
                                                                                   
          ResultSet rs1=st.executeQuery("select *from fees inner join student where student.status='living' and fees.month='"+month+"' and fees.IDNo='"+IDNo+"' and student.IDNo='"+IDNo+"'");
          if(rs1.first())
          {
            jButton3.setVisible(false);
            JOptionPane.showMessageDialog(null,"Fees is already paid by student for this month");
          }
        }
        catch(Exception e)
        {
          JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String IDNo=jTextField1.getText();
        String mobileNo=jTextField2.getText();
        String name=jTextField3.getText();
        String month=jTextField6.getText();
        String amount=jTextField7.getText();
        
        try
        {
          Connection con=ConnectionProvider.getCon();
          PreparedStatement ps=con.prepareStatement("insert into fees values(?,?,?,?,?)");
          ps.setString(1,IDNo);
          ps.setString(2,mobileNo);
          ps.setString(3,name);
          ps.setString(4,month);
          ps.setString(5,amount);
          ps.executeUpdate();
          tableDetails();
          JOptionPane.showMessageDialog(null,"Successfully Updated");
          clear();
        }
        catch(Exception e)
        {
          JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

